package cn.augrain.easy.qrcode.config;

/**
 * 矩形元素渐变方向
 *
 * @author biaoy
 * @since 2025/03/24
 */
public enum GradientDirection {
    /**
     * 上到下
     */
    TOP_BOTTOM,

    /**
     * 左到右
     */
    LEFT_RIGHT,

    /**
     * 左上到右下
     */
    TOP_LEFT_RIGHT_BOTTOM,

    /**
     * 左下到右上
     */
    LEFT_BOTTOM_TOP_RIGHT;

}
